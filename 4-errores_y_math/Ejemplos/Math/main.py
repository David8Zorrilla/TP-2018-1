import math

# funciones teoricas
print('Matematica Teorica')
print('===' * 2)
print('Redondea para arriba:', math.ceil(5.5))
print('Redondea para abaojo:', math.floor(5.5))
print('Factorial:', math.factorial(5))
print('Modulo flotante:', math.fmod(5, 10))
print('Modulo Entero:', 5 % 10)
num = [2, 5, 10]
print('Sumatoria:', math.fsum(num))
print('===' * 10)

# Potencias y logaritmos
print('Potencias y logaritmos')
print('===' * 2)
print('Exponent:', math.exp(2))
print('Logaritmo:', math.log(23, 10))
print('Logaritmo natural:', math.log1p(23))
print('Base 2:', math.log2(23))
print('Base 10:', math.log10(23))
print('Potencia:', math.pow(4, 2))
print('raiz:', math.sqrt(16))
print('===' * 10)

# Cosntantes
print('Constantes')
print('===' * 2)
print('Pi', math.pi)
print('e', math.e)
print('Tau', math.tau)
print('Infinito', math.inf)
print('Not a Number', math.nan)
print('===' * 10)

# Angulos
print('Angulos')
print('===' * 2)
print(math.degrees(math.pi/2))
print(math.radians(90))
print('===' * 10)

# Funciones trigonometricas
print('Trigonomericas')
print('===' * 2)
print(math.sin(3.1416))
print(math.hypot(2, 2))
print('===' * 10)
